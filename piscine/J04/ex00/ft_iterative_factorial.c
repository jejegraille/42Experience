int		ft_iterative_factorial(int nb)
{
	int nb2;

	nb2 = nb;
	if (nb2 > 0 && nb2 <= 12)
	{
		while (--nb > 0)
		{
			nb2 *= nb;
		}
	}
	else
		return (nb == 0 ? 1 : 0);
	return (nb2);
}

int		ft_str_is_lowercase(char *str)
{
	int i;
	int ret;

	i = 0;
	while (97 <= str[i] && str[i] <= 122)
	{
		i++;
	}
	if (str == 0)
		ret = 1;
	else if (str[i] == '\0')
		ret = 1;
	else
		ret = 0;
	return (ret);
}

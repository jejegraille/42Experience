int		ft_bouton(int i, int j, int k)
{
	int result[3];

	result[0] = (i - j) > 0 ? j : i;
	result[1] = (i - k) > 0 ? k : i;
	result[2] = (result[0] - result[1]) > 0 ? result[1] : result[0];
	return (result[2]);
}

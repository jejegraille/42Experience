char	*ft_strstr(char *str, char *to_find)
{
	int i;
	int i2;

	i = 0;
	i2 = 0;
	while (str[i] != '\0')
	{
		while (to_find[i2] == str[i + i2] && str[i + i2] != '\0')
			i2++;
		if (to_find[i2] == '\0')
			return (str + i);
		else
			i2 = 0;
		i++;
	}
	return (0);
}

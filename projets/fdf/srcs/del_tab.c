#include <fdf.h>

void	del_tab(t_fdf *fdf)
{
	int y;

	if (fdf->height > 0)
	{
		y = 0;
		while (y < fdf->height)
			ft_memdel((void **)&((fdf->map)[y++]));
		ft_memdel((void **)&(fdf->map));
	}
}

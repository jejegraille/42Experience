#include "libft.h"

void	ft_debug(int bug, int need)
{
	ft_putchar('\n');
	if (need == 1)
		ft_putstr("-------------");
	ft_putnbr(bug);
	if (need == 1)
		ft_putstr("-------------");
	ft_putchar('\n');
}

#include <operator.h>

void		pa(t_lst **a, t_lst **b)
{
	t_lst	*a1;
	t_lst	*b1;

	a1 = *a;
	b1 = *b;
	if (b1 != NULL)
	{
		*b = (*b)->next;
		if (*b != NULL)
			(*b)->back = NULL;
		if (a1 != NULL)
			a1->back = b1;
		b1->next = a1;
		b1->back = NULL;
		*a = b1;
	}
}

#include <ft_ls.h>
#include <unistd.h>

int			is_exe(mode_t mod, char who)
{
	int c;

	c = mod % 8;
	if (!(c == 0 || c == 2 || c == 4 || c == 6))
		if (who == 'a' || who == '-')
			return (1);
	c = (mod / 8) % 8;
	if (!(c == 0 || c == 2 || c == 4 || c == 6))
		if (who == 'g' || who == '-')
			return (1);
	c = (mod / (8 * 8)) % 8;
	if (!(c == 0 || c == 2 || c == 4 || c == 6))
		if (who == 'u' || who == '-')
			return (1);
	return (0);
}

int	ft_nbrlen(int nbr)
{
	int		len;
	long	n;

	len = 1;
	n = nbr;
	if (nbr < 0)
	{
		n *= -1;
		len++;
	}
	while (n > 9)
	{
		n /= 10;
		len++;
	}
	return (len);
}

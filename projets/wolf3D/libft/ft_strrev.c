char	*ft_strrev(char *str)
{
	int		c;
	char	tmp;
	int		i;

	i = 0;
	c = 0;
	while (str[c] != '\0')
		c++;
	c--;
	while (i <= c / 2)
	{
		tmp = str[i];
		str[i] = str[c - i];
		str[c - i] = tmp;
		i++;
	}
	return (str);
}

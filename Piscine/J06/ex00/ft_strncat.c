char	*ft_strncat(char *dest, char *src, int nb)
{
	int i;
	int i2;

	i = 0;
	i2 = 0;
	while (dest[i] != '\0')
		i++;
	while (src[i2] != '\0' && nb > 0)
	{
		dest[i + i2] = src[i2];
		i2++;
		nb--;
	}
	dest[i + i2] = '\0';
	return (dest);
}

int		ft_any(char **tab, int (*f)(char*))
{
	int i;
	int ok;

	ok = 0;
	i = 0;
	while (tab[i] != 0)
	{
		ok += f(tab[i]);
		i++;
	}
	return (ok > 0 ? 1 : 0);
}

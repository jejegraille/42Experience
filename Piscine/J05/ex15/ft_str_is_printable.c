int		ft_str_is_printable(char *str)
{
	int i;
	int ret;

	i = 0;
	while (32 <= str[i] && str[i] <= 126)
	{
		i++;
	}
	if (str == 0)
		ret = 1;
	else if (str[i] == '\0')
		ret = 1;
	else
		ret = 0;
	return (ret);
}
